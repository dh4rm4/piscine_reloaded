void	ft_div_mod(int a, int b, int *div, int *mod)
{
	if (!mod || !div)
		return ;
	if (b)
	{
		*div = a / b;
		*mod = a % b;
	}
	else
	{
		*div = 0;
		*mod = 0;
	}
}
